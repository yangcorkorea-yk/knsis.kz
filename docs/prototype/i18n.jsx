// React Context + hook
const LangCtx = React.createContext({ lang: 'kz', t: TRANSLATIONS.kz });

function LangProvider({ lang, children }) {
  const value = React.useMemo(() => ({ lang, t: TRANSLATIONS[lang] || TRANSLATIONS.kz }), [lang]);
  return <LangCtx.Provider value={value}>{children}</LangCtx.Provider>;
}

function useT() {
  return React.useContext(LangCtx).t;
}

function useLang() {
  return React.useContext(LangCtx).lang;
}

Object.assign(window, { TRANSLATIONS, LangCtx, LangProvider, useT, useLang });
